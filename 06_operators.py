
a = 8
b = 4

# Airthmatic operators
print("The value of a+b  = ",a+b)
print("The value of a-b  = ",a-b)
print("The value of a*b  = ",a*b)
print("The value of a/b  = ",a/b) # This is known as true division, will return the float value.
print("The value of a//b  = ",a//b) # This is known as floor division, will return the integer value(only) after division.
print("The value of b-a  = ",b-a)
print("The value of |b-a|  = ",abs(b-a))


# Logical operators
